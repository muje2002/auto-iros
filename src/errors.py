def get_error_message(code: str, fallback_message: str = "") -> str:
    """에러 코드에 해당하는 한국어 메시지를 반환.

    Args:
        code: CODEF 에러 코드 (예: CF-12100)
        fallback_message: 매핑이 없을 때 사용할 기본 메시지

    Returns:
        사용자 친화적 한국어 에러 메시지
    """
    if code in ERROR_MESSAGES:
        return ERROR_MESSAGES[code]
    if fallback_message:
        return f"[{code}] {fallback_message}"
    return f"[{code}] 알 수 없는 오류가 발생했습니다."


def is_retryable_error(code: str) -> bool:
    """재시도 가능한 에러인지 판단.

    서버 오류, 타임아웃 등 일시적 장애는 재시도 가능.
    파라미터 오류, 인증 오류 등은 재시도 불가.
    """
    retryable_codes = {
        "CF-10000",  # 서버 연결 실패
        "CF-10001",  # 서버 무응답
        "CF-10002",  # 서버 오류
        "CF-09999",  # 시스템 오류
    }
    return code in retryable_codes
